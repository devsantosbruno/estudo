// correspondente em graus Celsius (baseado na f√≥rmula abaixo)

let fahrenheit = 212
let toCelsius = ((fahrenheit - 32) / 9) * 5
console.log('Fahrenheit to Celsius conversion: ' + toCelsius)

// now, reverse
let celsius = 100
let toFahrenheit = (celsius * 9) / 5 + 32
console.log('\nCelsius to Fahrenheit conversion: ' + toFahrenheit)
